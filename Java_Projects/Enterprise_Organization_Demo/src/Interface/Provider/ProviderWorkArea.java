    public void populatetbl1(){
       DefaultTableModel dtm= (DefaultTableModel)tbl1.getModel();
       dtm.setRowCount(0);
       
       for(Vaccine v : po.getVaccinedirectory().getVaccinedirectory()){
           Object row[] = new Object[2];
           row[0] = v;
           row[1] = v.getInventory();
           dtm.addRow(row);
       }
    }
    
    public void populatetbl2(){
        DefaultTableModel dtm= (DefaultTableModel)tbl2.getModel();
        dtm.setRowCount(0);
       
        for(Organization o : e.getOrganizationDirectory().getOrganizationList()){
            if(o.getName().equals("Satelite Clinic Organization")){
                Object row[]= new Object[2];
                row[0] = o;
                row[1] = o.getOrganizationID();
                dtm.addRow(row);
            }
           
       }
    }
    
    public void populatetbl3(){
        DefaultTableModel dtm= (DefaultTableModel)tbl3.getModel();
        dtm.setRowCount(0);
       
        for(ClinicOrganization o : po.getClinic()){
            Object row[]= new Object[2];
                row[0] = o;
                row[1] = o.getOrganizationID();
                dtm.addRow(row);
            
           
       }
    }

