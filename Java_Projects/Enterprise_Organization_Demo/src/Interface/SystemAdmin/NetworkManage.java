    public void populatetbl(){
        DefaultTableModel dtm= (DefaultTableModel)tbl.getModel();
        dtm.setRowCount(0);
        for(Network nt : b.getNetworkList())
        {
            Object row[]= new Object[1];
            row[0] = nt;
            dtm.addRow(row);
        }
    }

