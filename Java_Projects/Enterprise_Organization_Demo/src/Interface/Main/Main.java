    private void loginbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_loginbtnActionPerformed
        // TODO add your handling code here:
        UserAccount ua = system.getUseraccountdirectory().authenticateUser(t1.getText(), String.valueOf(t2.getPassword()));
        Enterprise e = null;
        Organization o = null;
        if (ua == null) {
            //Step2: Go inside each network to check each enterprise
            for (Network n : system.getNetworkList()) {
                //Step 2-a: Check against each enterprise
                for(State s : n.getStatelist()){
                    for (Enterprise et : s.getEnterpriseDirectory().getEnterpriseList()) {
                        ua = et.getUseraccountdirectory().authenticateUser(t1.getText(), String.valueOf(t2.getPassword()));
                        if (ua == null) {
                            //Step3: Check against each organization inside that enterprise
                            for (Organization og : et.getOrganizationDirectory().getOrganizationList()) {
                                ua = og.getUseraccountdirectory().authenticateUser(t1.getText(), String.valueOf(t2.getPassword()));
                                if (ua != null) {
                                    e = et;
                                    o = og;
                                    break;
                                }
                            }
                        } else {
                            e = et;
                            break;
                        }
                        if (o != null) {
                            break;
                        }
                    }
                    if (e != null) {
                        break;
                    }
                }
            }
        }

        if (ua == null) {
            JOptionPane.showMessageDialog(null, "Invalid Credentails!");
            return;
        } else {
            CardLayout layout = (CardLayout) p.getLayout();
            p.add("workArea", ua.getRole().createWorkArea(p, ua, o, e, system));
            layout.next(p);
            t1.setText("");
            t2.setText("");
        }
    }//GEN-LAST:event_loginbtnActionPerformed

