    private void deletevaccinActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletevaccinActionPerformed
        // TODO add your handling code here:
        int selectedrow= tbl1.getSelectedRow();
        if(selectedrow >=0){
            Vaccine v= (Vaccine)tbl1.getValueAt(selectedrow,0);
            po.getVaccinedirectory().getVaccinedirectory().remove(v);
            JOptionPane.showMessageDialog(null,"Deleted");
            populatetbl1();
            DB4OUtil.getInstance().storeSystem(b);
        }else{
            JOptionPane.showMessageDialog(null,"Please Select Any Row");
        }
    }//GEN-LAST:event_deletevaccinActionPerformed

    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        // TODO add your handling code here:
        int selectedrow= tbl1.getSelectedRow();
        if(selectedrow >=0)
        {
            Vaccine v= (Vaccine)tbl1.getValueAt(selectedrow,0);
            Scanner sc1 = new Scanner(t2.getText());
            if(!t1.getText().isEmpty()&&sc1.hasNextInt()&&Integer.parseInt(t2.getText())>0){
                po.getVaccinedirectory().getVaccinedirectory().remove(v);
                v.setInventory(Integer.parseInt(t2.getText()));
                v.setVaccinename(t1.getText());
                po.getVaccinedirectory().getVaccinedirectory().add(v);
                JOptionPane.showMessageDialog(null,"Updated");
                savebtn.setEnabled(false);
                populatetbl1();
                DB4OUtil.getInstance().storeSystem(b);
            }else{
                JOptionPane.showMessageDialog(null,"Please Select Any Row");
            }
        }
    }//GEN-LAST:event_savebtnActionPerformed


