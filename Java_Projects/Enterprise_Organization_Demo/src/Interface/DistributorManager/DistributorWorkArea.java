    public void populatetbl1(){
       DefaultTableModel dtm= (DefaultTableModel)tbl1.getModel();
       dtm.setRowCount(0);
       
       for(Vaccine v : po.getVaccinedirectory().getVaccinedirectory()){
           Object row[] = new Object[2];
           row[0] = v;
           row[1] = v.getInventory();
           dtm.addRow(row);
       }
    }
    private void addbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_addbtnActionPerformed
        // TODO add your handling code here:
        Vaccine v = new Vaccine();
        Scanner sc1 = new Scanner(t2.getText());
        if(!t1.getText().isEmpty()&&sc1.hasNextInt()&&Integer.parseInt(t2.getText())>0){
            v.setInventory(Integer.parseInt(t2.getText()));
            v.setVaccinename(t1.getText());
            po.getVaccinedirectory().getVaccinedirectory().add(v);
            populatetbl1();
            DB4OUtil.getInstance().storeSystem(b);
        }else{
            JOptionPane.showMessageDialog(null,"Invalid Inputs");
        }

    }//GEN-LAST:event_addbtnActionPerformed

    private void editbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_editbtnActionPerformed
        // TODO add your handling code here:
        int selectedrow= tbl1.getSelectedRow();
        if(selectedrow >=0){
            Vaccine v= (Vaccine)tbl1.getValueAt(selectedrow,0);
            t1.setText(v.getVaccinename());
            t2.setText(String.valueOf(v.getInventory()));
            savebtn.setEnabled(true);
        }else{
            JOptionPane.showMessageDialog(null,"Please Select Any Row");
        }

    }//GEN-LAST:event_editbtnActionPerformed

