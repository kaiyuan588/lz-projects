    public void populatetbl(){
        DefaultTableModel dtm = (DefaultTableModel)tbl.getModel();
        dtm.setRowCount(0);
        for(RealEstate r : a.getRealestateOnsale()){
            Object row[] = new Object[4];
            row[0] = r;
            row[1] = r.getAddress();
            row[2] = r.getUnitprice();
            row[3] = r.getFloorlist().get(0).getRoomlist().get(0).getRoomarea();
            dtm.addRow(row);
        }
    }

