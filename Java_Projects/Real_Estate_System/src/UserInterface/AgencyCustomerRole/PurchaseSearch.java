    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        // TODO add your handling code here:
        ArrayList<RealEstate> search = new ArrayList<RealEstate>();
        for(RealEstate r : a.getRealestateOnsale()){
            if(r.getAddress().contains(t1.getText())){
                search.add(r);
            }
        }
        DefaultTableModel dtm = (DefaultTableModel)tbl.getModel();
        dtm.setRowCount(0);
        for(RealEstate r : search){
            Object row[] = new Object[4];
            row[0] = r;
            row[1] = r.getAddress();
            row[2] = r.getUnitprice();
            row[3] = r.getFloorlist().get(0).getRoomlist().get(0).getRoomarea();
            dtm.addRow(row);
        }
    }//GEN-LAST:event_searchbtnActionPerformed

    private void detailbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_detailbtnActionPerformed
        // TODO add your handling code here:
        int selectedrow = tbl.getSelectedRow();
        if(selectedrow >= 0){
            RealEstate r = (RealEstate)tbl.getValueAt(selectedrow, 0);
            ViewDetail panel = new ViewDetail(b,p,r,a,c,ua);
            p.add("ViewDetail",panel);
            CardLayout layout =(CardLayout) p.getLayout();
            layout.next(p);
        }else{
            JOptionPane.showMessageDialog(null,"Please Select Any Row");
        }
    }//GEN-LAST:event_detailbtnActionPerformed


