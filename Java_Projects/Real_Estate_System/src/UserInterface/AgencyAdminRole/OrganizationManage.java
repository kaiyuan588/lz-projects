    private void createbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createbtnActionPerformed
        // TODO add your handling code here:
        Scanner sc = new Scanner(t1.getText());
        if(!t1.getText().isEmpty() && sc.hasNextInt()){
            if(e.getOrganizationDirectory().checkuniqueroganizationid(Integer.parseInt(t1.getText()))){
                if(cb1.getSelectedIndex()==0){
                    AgencyDeveloperAgentOrg eo = new AgencyDeveloperAgentOrg();
                    eo.setOrganizationID(Integer.parseInt(t1.getText()));
                    e.getOrganizationDirectory().getOrganizationList().add(eo);
                }
                if(cb1.getSelectedIndex()==1){
                    AgencySalesOrg so = new AgencySalesOrg();
                    so.setOrganizationID(Integer.parseInt(t1.getText()));
                    e.getOrganizationDirectory().getOrganizationList().add(so);
                }
                if(cb1.getSelectedIndex()==2){
                    AgencyCustomerOrg co = new AgencyCustomerOrg();
                    co.setOrganizationID(Integer.parseInt(t1.getText()));
                    e.getOrganizationDirectory().getOrganizationList().add(co);
                }
                populatetbl();
                Databank.getInstance().storeSystem(b);
            }else{
                JOptionPane.showMessageDialog(null,"Organization Exists");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Invalid Inputs");
        }
    }//GEN-LAST:event_createbtnActionPerformed

    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        int selectedrow= tbl.getSelectedRow();
        if(selectedrow >=0){
            Organization o = (Organization)tbl.getValueAt(selectedrow,0);
            e.getOrganizationDirectory().getOrganizationList().remove(o);
            populatetbl();
            Databank.getInstance().storeSystem(b);
        }else{
            JOptionPane.showMessageDialog(null,"Please Select Any Row");
        }
    }//GEN-LAST:event_deletebtnActionPerformed


