    public void populatetbl(){
        DefaultTableModel dtm= (DefaultTableModel)tbl.getModel();
        dtm.setRowCount(0);
        for(Organization o : e.getOrganizationDirectory().getOrganizationList()){
            Object row[] = new Object[2];
            row[0] = o;
            row[1] = o.getOrganizationID();
            dtm.addRow(row);
        }
    }

