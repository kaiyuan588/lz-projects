  public void populatetbl1(){
        DefaultTableModel dtm= (DefaultTableModel)tbl1.getModel();
        dtm.setRowCount(0);
        for(Land re : e.getCity().getLandtobesold()){
            if(re.isStatus() == true){    
                Object row[]= new Object[4];
                row[0] = re;
                row[1] = re.getArea();
                row[2] = re.getUnitprice();
                row[3] = re.getAddress();
                dtm.addRow(row);
            }
        }
    }
    public void populatetbl2(){
        DefaultTableModel dtm= (DefaultTableModel)tbl2.getModel();
        dtm.setRowCount(0);
        for(LandOrder o : e.getOrderlist()){
                Object row[]= new Object[3];
                row[0] = o;
                row[1] = o.getLand();
                row[2] = o.getOrderdate();
                dtm.addRow(row);
            
        }
    }
