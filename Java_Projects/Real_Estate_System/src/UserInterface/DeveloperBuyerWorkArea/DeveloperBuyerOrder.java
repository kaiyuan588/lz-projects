    private void orderbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_orderbtnActionPerformed
        // TODO add your handling code here:
        int selectedrow= tbl1.getSelectedRow();
        if(selectedrow >=0){
            Land r= (Land)tbl1.getValueAt(selectedrow,0);
            if(e.findLand(r.getAddress())==null){
                e.getLandlist().add(r);
                LandOrder o = new LandOrder(e, r.getLandowner(),r);
                DateFormat format = new SimpleDateFormat("dd/MM/yy HH:mm");
                Date date = new Date();
                o.setOrderid((int)(System.currentTimeMillis()%1000000000));
                o.setOrderdate(format.format(date));
                o.setPrice(r.getUnitprice());
                e.getOrderlist().add(o);
                r.getLandowner().getOrderlist().add(o);
                r.setStatus(false);
                Databank.getInstance().storeSystem(b);
                populatetbl2();
            }else{
                JOptionPane.showMessageDialog(null,"Land Has Been Ordered");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Please Select Any Row");
        }
    }//GEN-LAST:event_orderbtnActionPerformed

    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        int selectedrow= tbl2.getSelectedRow();
        if(selectedrow >=0){
            Land r= (Land)tbl2.getValueAt(selectedrow,1);
            LandOrder o = (LandOrder)tbl2.getValueAt(selectedrow,0);
            e.getLandlist().remove(r);
            e.getOrderlist().remove(o);
            r.getLandowner().getOrderlist().remove(o);
            populatetbl2();
            Databank.getInstance().storeSystem(b);
        }else{
            JOptionPane.showMessageDialog(null,"Please Select Any Row");
        }
    }//GEN-LAST:event_deletebtnActionPerformed

