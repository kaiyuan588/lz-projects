    public void populatetbl(){
        DefaultTableModel dtm  = (DefaultTableModel)tbl.getModel();
        dtm.setRowCount(0);
        for(AgentRequest rq : e.getAgentrequestlist()){
            Object row[] = new Object[4];
            row[0] = rq;
            row[1] = rq.getRealestate();
            row[2] = rq.getAgency();
            if(rq.getStatus()==null){
                row[3] = "Yet Set";
            }else{
                if(rq.getStatus()==true){
                    row[3] = "Approved";
                }else{
                    row[3] = "Rejected";
                }
            }
            dtm.addRow(row);
        }
        
    }

