    private void statusbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_statusbtnActionPerformed
        // TODO add your handling code here:
        int selectedrow = tbl.getSelectedRow();
        if(selectedrow >= 0){
            AgentRequest rq = (AgentRequest)tbl.getValueAt(selectedrow, 0);
            rq.getAgency().getAgentrequestlist().remove(rq);
            if(cb1.getSelectedIndex()==0){
                rq.setStatus(true);
                rq.getAgency().getRealestateOnsale().add(rq.getRealestate());
            }else{
                rq.setStatus(false);
            }
            rq.getAgency().getAgentrequestlist().add(rq);
            populatetbl();
            Databank.getInstance().storeSystem(b);
        }else{
            JOptionPane.showMessageDialog(null, "Please Select Any Row");
        }
    }//GEN-LAST:event_statusbtnActionPerformed


