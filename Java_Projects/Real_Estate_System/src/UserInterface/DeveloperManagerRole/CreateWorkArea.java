    public void populatetbl(){
        DefaultTableModel dtm= (DefaultTableModel)tblLand.getModel();
        dtm.setRowCount(0);
        for(Land re : e.getLandlist()){
                Object row[]= new Object[4];
                row[0] = re;
                row[1] = re.getArea();
                row[2] = re.getUnitprice();
                row[3] = re.getAddress();
                dtm.addRow(row);
            
        }
    }
