    private void createbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createbtnActionPerformed
        // TODO add your handling code here:
        if(!t1.getText().isEmpty()){
            if(b.checkUniqueNetwork(t1.getText())){
                CountryNetwork nt = b.createAndAddNetwork();
                nt.setName(t1.getText());
//                JOptionPane.showMessageDialog(null,"Created");
                populatetbl();
                Databank.getInstance().storeSystem(b);
            }else{
                JOptionPane.showMessageDialog(null,"Country Network Exists");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Empty Input!");
        }
        
    }//GEN-LAST:event_createbtnActionPerformed

    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        int selectedrow= tbl.getSelectedRow();
        if(selectedrow >=0){
            CountryNetwork nt= (CountryNetwork)tbl.getValueAt(selectedrow,0);
            b.getNetworkList().remove(nt);
//            JOptionPane.showMessageDialog(null,"Deleted");
            populatetbl();
            Databank.getInstance().storeSystem(b);
        }else{
            JOptionPane.showMessageDialog(null,"Please Select Any Row");
        }
    }//GEN-LAST:event_deletebtnActionPerformed


