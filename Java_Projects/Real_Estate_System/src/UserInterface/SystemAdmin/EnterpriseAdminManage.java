    public void populatetbl(Enterprise e){
        DefaultTableModel dtm= (DefaultTableModel)tbl.getModel();
        dtm.setRowCount(0);
        for (UserAccount ua : e.getUseraccountdirectory().getUserAccountList()){
            Object row[] = new Object[3];
            row[0] = e;
            row[1] = ua;
            row[2] = ua.getPerson();
            dtm.addRow(row);
        }
    }

