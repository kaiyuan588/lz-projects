    private void createbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createbtnActionPerformed
        // TODO add your handling code here:
        if(!t1.getText().isEmpty() && t2.getPassword().length != 0 && !t3.getText().isEmpty()){
            if(cb4.getSelectedItem()!=null){
                CountryNetwork nt = b.findNetwork((String)cb1.getSelectedItem());
                State s = nt.findState((String)cb2.getSelectedItem());
                City c = s.findCity((String)cb3.getSelectedItem());
                Enterprise e = c.getEnterpriseDirectory().findEnterprise((String)cb4.getSelectedItem());
                if(e.getUseraccountdirectory().checkIfUsernameIsUnique(t1.getText())){
                    Person p = e.getPersondirectory().findPersonByName(t3.getText());
                    if(p != null){
                        UserAccount ua = e.getUseraccountdirectory().createUserAccount(t1.getText(), String.valueOf(t2.getPassword()), p, new EnterpriseAdmin());
                        p.getUseraccount().add(ua);
                    }else{
                        Person pp = new Person();
                        pp.setName(t3.getText());
                        UserAccount ua = e.getUseraccountdirectory().createUserAccount(t1.getText(), String.valueOf(t2.getPassword()), pp, new EnterpriseAdmin());
                        pp.getUseraccount().add(ua);
                    }
                    populatetbl(e);
                    Databank.getInstance().storeSystem(b);
                }else{
                    JOptionPane.showMessageDialog(null,"Username Exsits");
                }
            }else{
                JOptionPane.showMessageDialog(null,"Empty Enterprise");
            }
        }else{
            JOptionPane.showMessageDialog(null,"Empty Inputs");
        }
    }//GEN-LAST:event_createbtnActionPerformed

    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        int selectedrow= tbl.getSelectedRow();
        if(selectedrow >=0){
            Enterprise e = (Enterprise)tbl.getValueAt(selectedrow,0);
            UserAccount ua = (UserAccount)tbl.getValueAt(selectedrow,1);
            Person p = (Person)tbl.getValueAt(selectedrow,2);
            e.getUseraccountdirectory().getUserAccountList().remove(ua);
            p.getUseraccount().remove(ua);
//            JOptionPane.showMessageDialog(null,"Deleted");
            populatetbl(e);
            Databank.getInstance().storeSystem(b);
        }else{
            JOptionPane.showMessageDialog(null,"Please Select Any Row");
        }
    }//GEN-LAST:event_deletebtnActionPerformed


