    public void populatetbl(){
        DefaultTableModel dtm  = (DefaultTableModel)tbl.getModel();
        dtm.setRowCount(0);
        for(CustomerRequest rq : a.getCustomerrequestlist()){
            Object row[] = new Object[6];
            row[0] = rq;
            row[1] = rq.getCustomeraccount();
            row[2] = rq.getRequesttype();
            row[3] = rq.getRealestate().getConstruction().getName();
            row[4] = rq.getRoom();
            if(rq.getStatus()==null){
                row[5] = "Yet Set";
            }else{
                if(rq.getStatus()==true){
                    row[5] = "Approved";
                }else{
                    row[5] = "Rejected";
                }
            }
            dtm.addRow(row);
        }
    }

