    private void requestbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_requestbtnActionPerformed
        // TODO add your handling code here:
        int selectedrow = tbl.getSelectedRow();
        if(selectedrow >= 0){
            CustomerRequest rq = (CustomerRequest)tbl.getValueAt(selectedrow, 0);
            if(cb1.getSelectedIndex()==0){
                rq.setStatus(true);
                if(rq.getRequesttype()==CustomerRequestType.Purchase){
                    QuoteManage panel = new QuoteManage(b,p,a,as,rq);
                    p.add("Quote",panel);
                    CardLayout layout =(CardLayout) p.getLayout();
                    layout.next(p);
                }
            }else{
                rq.setStatus(false);
            }
            populatetbl();
            Databank.getInstance().storeSystem(b);
        }else{
            JOptionPane.showMessageDialog(null, "Please Select Any Row");
        }
    }//GEN-LAST:event_requestbtnActionPerformed


