    private void btnSaveActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSaveActionPerformed
        // TODO add your handling code here:
        Boolean e1 = null;
        Boolean e2 = null;
        if(txtProductName.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Product Name Required!");
            e1 = false;
        }else{e1 = true;}
        if(txtProductID.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Product ID Required!");
            e2 = false;
        }else{e2 = true;}
        if(e1&&e2){
            Product pt = new Product();
            sp.getProductcatalog().deleteProduct(pr);
            b.getProductdirectory().deleteProduct(pr);
            pt.setProductname(txtProductName.getText());
            pt.setProductid(Integer.parseInt(txtProductID.getText()));
            sp.getProductcatalog().getproduct().add(pt);
            b.getProductdirectory().getproduct().add(pt);
            Configure.ExportProducts(b.getProductdirectory());
            btnSave.setEnabled(false);
            btnEdit.setEnabled(true);
            txtProductName.setEnabled(false);
            txtProductID.setEnabled(false);
            JOptionPane.showMessageDialog(null,"Product updated!");
        }
    }//GEN-LAST:event_btnSaveActionPerformed

