 public void refreshTable()
    {

        DefaultTableModel dtm = (DefaultTableModel) tbProduct.getModel();
        dtm.setRowCount(0);

        for(Product s : s.getProductcatalog().getproduct())
        {
            Object row[] = new Object[2];
            row[0] = s;
            row[1] = s.getProductname();
            dtm.addRow(row);
        }
    }
    
