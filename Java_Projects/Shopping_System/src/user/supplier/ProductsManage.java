    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        // TODO add your handling code here:
        ProductCatalog pc = s.getProductcatalog();
        int search = Integer.parseInt(jTextField1.getText());
        Product results = pc.searchProduct(search);
        if(results == null){JOptionPane.showMessageDialog(null, "Can not find such Product");}
        else{
                DefaultTableModel dtm = (DefaultTableModel)tbProduct.getModel();
                dtm.setRowCount(0);
                
                Object row[] = new Object[2];
            row[0] = results;
            row[1] = results.getProductname();
            dtm.addRow(row);
                
        
     
    }//GEN-LAST:event_searchbtnActionPerformed
    }
    private void deletebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_deletebtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbProduct.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please selected a row from table first");
        }else{
            Product product1 = (Product)tbProduct.getValueAt(selectedRow, 0);
            s.getProductcatalog().deleteProduct(product1);
            b.getProductdirectory().deleteProduct(product1);
            refreshTable();
            JOptionPane.showMessageDialog(null, "Deleted");
        }
    }//GEN-LAST:event_deletebtnActionPerformed

