    private void viewbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewbtnActionPerformed
        // TODO add your handling code here:
        int selectedRow = tbProduct.getSelectedRow();
        if(selectedRow<0){
            JOptionPane.showMessageDialog(null, "Please selected a row from table first!","warning",JOptionPane.WARNING_MESSAGE);
        }
        else{
            Product pr = (Product)tbProduct.getValueAt(selectedRow, 0);
            ViewProducts panel = new ViewProducts(pr,p,b,s);
            p.add("ViewProducts", panel);
            CardLayout layout = (CardLayout) p.getLayout();
            layout.next(p);
        }
    }//GEN-LAST:event_viewbtnActionPerformed

