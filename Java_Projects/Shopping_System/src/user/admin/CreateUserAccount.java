    private void createbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createbtnActionPerformed
        // TODO add your handling code here:
        Boolean e1 = null;
        Boolean e2 = null;
        Boolean e3 = null;
        Boolean e4 = null;
        
        if(cb1.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Please Select A Name");
            e1 = false;
        }else{e1 = true;}
        if(t1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Username Required");
            e2 = false;
        }else{e2 = true;}
        if(String.valueOf(t2.getPassword()).isEmpty()){
            JOptionPane.showMessageDialog(null,"Password Required");
            e3 = false;
        }else{e3 = true;}
        
        
        UserAccount target = null;
        for(int i=0; i<pa.getUseraccountdirectory().getUseraccount().size();i++){
            if(pa.getUseraccountdirectory().getUseraccount().get(i).getUsername().equals(t1.getText())){
                target = pa.getUseraccountdirectory().getUseraccount().get(i);
                break;
            }
        }
        if(target!=null){
            JOptionPane.showMessageDialog(null,"Username Exsits");
            e4 = false;
        }else{e4 = true;}
        if(e1&&e2&&e3&&e4){
            String fnm = (String)cb1.getSelectedItem();
            String usn = t1.getText();
            String pwd = String.valueOf(t2.getPassword());
            String acr = null;
            for(int i=0; i<pa.getPersondirectory().getPerson().size(); i++){
                if(pa.getPersondirectory().getPerson().get(i).getFullname().equals(fnm)){
                    acr = pa.getPersondirectory().getPerson().get(i).getPersonrole();
                }
            }
            /*Add useraccount to useraccountdirectory*/
            UserAccount usa = pa.getUseraccountdirectory().addUser();
            usa.setFullname(fnm);
            usa.setUsername(usn);
            usa.setPassword(pwd);
            usa.setAccountrole(acr);
            /*Add useraccount to rolemanage*/
            if(usa.getAccountrole().equals("Admin")){
                for(int i=0; i<pa.getRolemanage().getAdmin().size(); i++){
                    if(usa.getFullname().equals(pa.getRolemanage().getAdmin().get(i).getFullname())){
                        pa.getRolemanage().getAdmin().get(i).getUseraccount().add(usa);
                        break;
                    }
                }
            }
            if(usa.getAccountrole().equals("Supplier")){
                for(int i=0; i<pa.getRolemanage().getSupplier().size(); i++){
                    if(usa.getFullname().equals(pa.getRolemanage().getSupplier().get(i).getSuppliername())){
                        pa.getRolemanage().getSupplier().get(i).getUseraccount().add(usa);
                        break;
                    }
                }
            }
            if(usa.getAccountrole().equals("Salesman")){
                for(int i=0; i<pa.getRolemanage().getSalesman().size(); i++){
                    if(usa.getFullname().equals(pa.getRolemanage().getSalesman().get(i).getFullname())){
                        pa.getRolemanage().getSalesman().get(i).getUseraccount().add(usa);
                        break;
                    }
                }
            }
            Configure.ExportUser(pa.getUseraccountdirectory());
            JOptionPane.showMessageDialog(null,"Created");
            cb1.setSelectedIndex(0);
            t1.setText("");
            t2.setText("");
            
                
        }
    }//GEN-LAST:event_createbtnActionPerformed


