    public void populatetbl(){
        DefaultTableModel dtm= (DefaultTableModel)tbl.getModel();
        dtm.setRowCount(0);
        for(Admin sm: pa.getRolemanage().getAdmin()){
            Object row[]= new Object[2];
            row[0] = sm;
            ArrayList<String> psus = new ArrayList();
            for(int i=0; i<pa.getUseraccountdirectory().getUseraccount().size(); i++){
                if(pa.getUseraccountdirectory().getUseraccount().get(i).getFullname().equals(sm.getFullname())){
                    psus.add(pa.getUseraccountdirectory().getUseraccount().get(i).getUsername());
                }
            }
            String adusn = String.join(",",psus);
            row[1] = adusn;
            dtm.addRow(row);
        }
    }

