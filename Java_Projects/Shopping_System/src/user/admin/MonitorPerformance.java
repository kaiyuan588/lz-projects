    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        // TODO add your handling code here:
        if(cb.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Please Select Any Way");
        }
        if(cb.getSelectedIndex()==1){
            TotalRevenue panel = new TotalRevenue(b,pp,pa);
            pp.add("monitor",panel);
            CardLayout layout =(CardLayout) pp.getLayout();
            layout.next(pp);
        }
        if(cb.getSelectedIndex()==2){
            RevenueByMarket panel = new RevenueByMarket(b,pp,pa);
            pp.add("monitor",panel);
            CardLayout layout =(CardLayout) pp.getLayout();
            layout.next(pp);
        }
        if(cb.getSelectedIndex()==3){
            TopTenPerson panel = new TopTenPerson(b,pp,pa);
            pp.add("monitor",panel);
            CardLayout layout =(CardLayout) pp.getLayout();
            layout.next(pp);
        }
        if(cb.getSelectedIndex()==4){
            AboveTarget panel = new AboveTarget(b,pp,pa);
            pp.add("monitor",panel);
            CardLayout layout =(CardLayout) pp.getLayout();
            layout.next(pp);
        }
        if(cb.getSelectedIndex()==5){
            BelowTarget panel = new BelowTarget(b,pp,pa);
            pp.add("monitor",panel);
            CardLayout layout =(CardLayout) pp.getLayout();
            layout.next(pp);
        }
        if(cb.getSelectedIndex()==6){
            TopProducts panel = new TopProducts(b,pp,pa);
            pp.add("monitor",panel);
            CardLayout layout =(CardLayout) pp.getLayout();
            layout.next(pp);
        }
        
    }//GEN-LAST:event_searchbtnActionPerformed


