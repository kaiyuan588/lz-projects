    private void createbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_createbtnActionPerformed
        // TODO add your handling code here:
        Boolean e1 = null;
        Boolean e2 = null;
        if(t1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Full Name Required");
            e1 = false;
        }else{
            e1 = true;
        }
        if(cb.getSelectedIndex()==0){
            JOptionPane.showMessageDialog(null,"Role Required");
            e2 = false;
        }else{
            e2 = true;
        }
        if(e1&&e2){
            Person ps = new Person();
            ps.setFullname(t1.getText());
            ps.setPersonrole((String)cb.getSelectedItem());
            pa.getPersondirectory().getPerson().add(ps);
            JOptionPane.showMessageDialog(null,"Created");
            if(cb.getSelectedIndex()==1){
                Admin ad = new Admin();
                ad.setFullname(ps.getFullname());
                pa.getRolemanage().getAdmin().add(ad);
                Configure.ExportAdmin(pa.getRolemanage());
            }
            if(cb.getSelectedIndex()==2){
                Supplier sp = new Supplier();
                sp.setSuppliername(ps.getFullname());
                pa.getRolemanage().getSupplier().add(sp);
                Configure.ExportSupplier(pa.getRolemanage());
            }
            if(cb.getSelectedIndex()==3){
                Salesman sm = new Salesman();
                sm.setFullname(ps.getFullname());
                pa.getRolemanage().getSalesman().add(sm);
                Configure.ExportSalesman(pa.getRolemanage());
            }
        }
    }//GEN-LAST:event_createbtnActionPerformed

