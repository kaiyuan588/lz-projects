    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
        Boolean e1 = null;
        Boolean e2 = null;
        Boolean e3 = null;
        if(t2.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Username Required");
            e2 = false;
        }else{e2 = true;}
        if(String.valueOf(t3.getPassword()).isEmpty()){
            JOptionPane.showMessageDialog(null,"Password Required");
            e3 = false;
        }else{e3 = true;}
        UserAccount target = null;
        for(int i=0; i<pa.getUseraccountdirectory().getUseraccount().size();i++){
            if(pa.getUseraccountdirectory().getUseraccount().get(i).getUsername().equals(t1.getText())&&!ua.getUsername().equals(t1.getText())){
                target = pa.getUseraccountdirectory().getUseraccount().get(i);
                break;
            }
        }
        if(target!=null){
            JOptionPane.showMessageDialog(null,"Username Exsits");
            e1 = false;
        }else{e1 = true;}
        
        
        if(e1&&e2&&e3){
            String ar = ua.getAccountrole();
            pa.getUseraccountdirectory().deleteUser(ua);
            String un = t1.getText();
            String fn = t2.getText();
            String pd = String.valueOf(t3.getPassword());
            UserAccount u = pa.getUseraccountdirectory().addUser();
            u.setAccountrole(ar);
            u.setUsername(un);
            u.setFullname(fn);
            if(pd.equals("*********")){
                u.setPassword(ua.getPassword());
            }else{
                    u.setPassword(pd);
            }
            JOptionPane.showMessageDialog(null,"Updated");
        }
        
        Configure.ExportUser(pa.getUseraccountdirectory());
    }//GEN-LAST:event_updatebtnActionPerformed


