    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
        b.getMarketoffercatalog().deleteMarketOffer(mo);
        Boolean e1 = null;
        Boolean e2 = null;
        Boolean e3 = null;
        Boolean e4 = null;
        Boolean e5 = null;
        Scanner sc1 = new Scanner(t1.getText());
        Scanner sc2 = new Scanner(t2.getText());
        Scanner sc3 = new Scanner(t3.getText());
        
        if(!sc1.hasNextDouble()){e1 = false;}else{e1 = true;}
        if(!sc2.hasNextDouble()){e2 = false;}else{e2 = true;}
        if(!sc3.hasNextDouble()){e3 = false;}else{e3 = true;}
        
        if(b.getMarketoffercatalog().findMarketOffer((String)cb2.getSelectedItem(), Integer.parseInt(t4.getText()))==null){
            e4 = true;
        }else{
            JOptionPane.showMessageDialog(null,"Market Offer Existed");
            e4 = false;
        }
        
        if(e1&&e2&&e3){
            if(Double.parseDouble(t1.getText())<Double.parseDouble(t3.getText()) && Double.parseDouble(t3.getText())<Double.parseDouble(t2.getText())){
                e5 = true;
            }else{
                JOptionPane.showMessageDialog(null,"Please Follow: Floor Price < Target Price < Ceiling Price");
                e5 = false;
            }
        }else{
            JOptionPane.showMessageDialog(null,"Invalid Price");
        }
        
        if(e5&&e4){
            MarketOffer mo = new MarketOffer();
            mo.setProductid(Integer.parseInt(t4.getText()));
            mo.setMarketname((String)cb2.getSelectedItem());
            mo.setFloor(Double.parseDouble(t1.getText()));
            mo.setCeiling(Double.parseDouble(t2.getText()));
            mo.setTarget(Double.parseDouble(t3.getText()));
            b.getMarketoffercatalog().getMarketoffer().add(mo);
            for(int i=0; i<b.getOrderlist().getOrder().size(); i++){
                if(b.getOrderlist().getOrder().get(i).getProductid()==mo.getProductid()&&b.getOrderlist().getOrder().get(i).getMarketname().equals(mo.getMarketname())){
                    b.getOrderlist().getOrder().get(i).setTargetprice(mo.getTarget());
                }
            }
            Configure.ExportOrder(b.getOrderlist());
            Configure.ExportMarketOffer(b.getMarketoffercatalog());
            JOptionPane.showMessageDialog(null,"Updated");
        }
    }//GEN-LAST:event_updatebtnActionPerformed


