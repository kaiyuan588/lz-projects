    private void btnCustomerActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCustomerActionPerformed
        if(sm.getMarketname()==null){
            JOptionPane.showMessageDialog(null, "Admin Should Configure The Market First");
        }else{
            RegisterCustomer panel = new RegisterCustomer(b, ua, p, pa,sm);
            p.add("RegisterCustomer", panel);
            CardLayout layout = (CardLayout) p.getLayout();
            layout.next(p);   
        }// TODO add your handling code here:
    }//GEN-LAST:event_btnCustomerActionPerformed

