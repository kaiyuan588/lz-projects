package Profile;

/**
 *
 * @author Ruby
 */
public class FinancialAccount {
    private String BankFA;
    private int CheckNumber;
    private int SavingNumber;
    private String CreationDateC;
    private String CreationDateS;
    private String StatusC;
    private String StatusS;
    private String AccounttypeC;
    private String AccounttypeS;

    public String getBankFA() {
        return BankFA;
    }

    public void setBankFA(String BankFA) {
        this.BankFA = BankFA;
    }

    public int getCheckNumber() {
        return CheckNumber;
    }

    public void setCheckNumber(int CheckNumber) {
        this.CheckNumber = CheckNumber;
    }

    public int getSavingNumber() {
        return SavingNumber;
    }

    public void setSavingNumber(int SavingNumber) {
        this.SavingNumber = SavingNumber;
    }

    

    public String getCreationDateC() {
        return CreationDateC;
    }

    public void setCreationDateC(String CreationDateC) {
        this.CreationDateC = CreationDateC;
    }

    public String getCreationDateS() {
        return CreationDateS;
    }

    public void setCreationDateS(String CreationDateS) {
        this.CreationDateS = CreationDateS;
    }

    public String getStatusC() {
        return StatusC;
    }

    public void setStatusC(String StatusC) {
        this.StatusC = StatusC;
    }

    public String getStatusS() {
        return StatusS;
    }

    public void setStatusS(String StatusS) {
        this.StatusS = StatusS;
    }

    public String getAccounttypeC() {
        return AccounttypeC;
    }

    public void setAccounttypeC(String AccounttypeC) {
        this.AccounttypeC = AccounttypeC;
    }

    public String getAccounttypeS() {
        return AccounttypeS;
    }

    public void setAccounttypeS(String AccounttypeS) {
        this.AccounttypeS = AccounttypeS;
    }
    
}
