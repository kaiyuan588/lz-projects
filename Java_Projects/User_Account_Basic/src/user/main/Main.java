    private void isValidUser(){
        UserAccount target = null;
        for(int i=0; i<b.getUseraccountdirectory().getUseraccount().size();i++){
            if(t1.getText().equals(b.getUseraccountdirectory().getUseraccount().get(i).getUsername())){
                target = b.getUseraccountdirectory().getUseraccount().get(i);
                break;
            }
        }
        if(target==null){
            JOptionPane.showMessageDialog(null,"Username Not Found");
            t1.setText("");
            t2.setText("");
        }else{
            if(target.isStatus()){
                if(target.getPassword().equals(b.passwordtohash(String.valueOf(t2.getPassword())))){
                    if (target.getAccountrole().equals("System Admin")){
                        AdminInfo panel = new AdminInfo(b,target,p);
                        p.add("adminmanagement",panel);
                        CardLayout layout =(CardLayout) p.getLayout();
                        layout.next(p);
                    }else{
                        HRInfo panel = new HRInfo(b,target,p);
                        p.add("hrmanagement",panel);
                        CardLayout layout =(CardLayout) p.getLayout();
                        layout.next(p);
                    }
                }else{
                        JOptionPane.showMessageDialog(null,"Wrong Password");
                        t2.setText("");
                }
            }else{
                JOptionPane.showMessageDialog(null,"Account Disabled");
                t1.setText("");
                t2.setText("");
            }
        }
        
    }
