    private void updatebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_updatebtnActionPerformed
        // TODO add your handling code here:
        Boolean e1 = null;
        Boolean e2 = null;
        if(t1.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"First Name Required");
            e1 = false;
        }else{e1 = true;}
        if(t2.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Last Name Required");
            e2 = false;
        }else{e2 = true;}
        if(e1&&e2){
            b.getPersondirectory().deletePerson(ps);
            String fn = t1.getText();
            String ln = t2.getText();
            Person ps = b.getPersondirectory().addPerson();
            ps.setFirstname(fn);
            ps.setLastname(ln);
            
            try{   
                BufferedWriter fw = new BufferedWriter(new FileWriter("person.txt"));
                for(Person line : b.getPersondirectory().getPerson()){
                    String[] a = new String[2];
                    a[0] = line.getFirstname();
                    a[1] = line.getLastname();
                    String thisline=Arrays.toString(a);
                    fw.write(thisline.replace("[", "").replace("]", "").replace(", ",","));
                    fw.newLine();
                }     
                fw.close();    
            }catch(Exception e){System.out.println(e);} 
            System.out.println("Success...");
            JOptionPane.showMessageDialog(null,"Updated");
        }
    }//GEN-LAST:event_updatebtnActionPerformed


