    private void savebtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_savebtnActionPerformed
        // TODO add your handling code here:
        int planeid= Integer.parseInt(t1.getText());
        boolean status = c1.isSelected();
        String manufact = (String)cb1.getSelectedItem();
        int yearmade = Integer.parseInt(t2.getText());
        int totalseats = Integer.parseInt(t3.getText());
        String airliner = (String)cb2.getSelectedItem();
        int model = Integer.parseInt((String)cb3.getSelectedItem());
        String airport= t5.getText();
        String expdate= t6.getText();
        Boolean e1=null;
        Boolean e2=null;
        if (yearmade<=1990 || yearmade>2017) {
            JOptionPane.showMessageDialog(null,"Invalid Year");
            e2=false;
            
        }else{e2=true;}
        
        Date date = null;
        try {
                DateFormat formatter = new SimpleDateFormat("yyyy-MM-dd");
                formatter.setLenient(false);
                date = formatter.parse(expdate);
            } catch (ParseException e) { 
                e.printStackTrace();
            }
        
        if(date == null){
            JOptionPane.showMessageDialog(null,"Invalid Date");
            e1= false;
        }else{
            e1=true;
        }
        if(e1&&e2){
        PlaneInfo p=ht.addPlanes();
        p.setId(planeid);
        p.setAvail(status);
        p.setMft(manufact);
        p.setMadeyear(yearmade);
        p.setAirliner(airliner);
        p.setSeats(totalseats);
        p.setAirport(airport);
        p.setModel(model);
        p.setExpdate(expdate);
        p.setUpdate(System.currentTimeMillis());
        JOptionPane.showMessageDialog(null,"Created");
        
        t1.setText("");
        t2.setText("");
        t3.setText("");
        
        t5.setText("");
        t6.setText("");
        c1.setSelected(false);
        c2.setSelected(false);
        cb1.setSelectedIndex(0);
        cb2.setSelectedIndex(0);
        cb3.setSelectedIndex(0);
        
        try{   
                BufferedWriter fw = new BufferedWriter(new FileWriter("file.txt"));
                for(PlaneInfo line : ht.getHistory()){
                    
                    String[] a = new String[10];
                    a[0]=String.valueOf(line.getId());
                    a[1]=String.valueOf(line.isAvail());
                    a[2]=line.getMft();
                    a[3]=String.valueOf(line.getMadeyear());
                    a[4]=String.valueOf(line.getSeats());
                    a[5]=String.valueOf(line.getModel());
                    a[6]=line.getAirliner();
                    a[7]=String.valueOf(line.getUpdate());
                    a[8]=line.getAirport();
                    a[9]=line.getExpdate();
                    String thisline=Arrays.toString(a);
                    fw.write(thisline.replace("[", "").replace("]", "").replace(", ",","));
                    fw.newLine();
                }     
                fw.close();    
            }catch(Exception e){System.out.println(e);}    
          System.out.println("Success..."); 
        
        }
        
        
        
        
                
    }//GEN-LAST:event_savebtnActionPerformed

