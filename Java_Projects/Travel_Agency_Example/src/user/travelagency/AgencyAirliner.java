    private void searchbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchbtnActionPerformed
        // TODO add your handling code here:
        DefaultTableModel dtm= (DefaultTableModel)tbl.getModel();
        dtm.setRowCount(0);
        Boolean e1 = null;
        Boolean e2 = null;
        Boolean e3 = null;
        Boolean e4 = null;
        if(tf.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Departure City Required");
            e1= false;
        }else{e1=true;}
        
        if(tt.getText().isEmpty()){
            JOptionPane.showMessageDialog(null,"Arrival City Required");
            e2= false;
        }else{e2=true;}
        if(e1&&e2){
            String fc = tf.getText();
            String tc = tt.getText();
            ((DefaultTableModel)tbl.getModel()).setNumRows(0);
            ArrayList<Flight> targets = new ArrayList<Flight>();
            ArrayList<Flight> list = a.getAirliner();
            for(int i=0 ; i<list.size();i++){
                if(list.get(i).getFrom().equals(fc)){e3=true;}else{e3=false;}
                if(list.get(i).getTo().equals(tc)){e4=true;}else{e4=false;}
                if(e3&&e4){
                    Flight target = list.get(i);
                    Object row[]= new Object[4];
                    row[0] = target;
                    row[1] = target.getDepart();
                    row[2] = target.getArrive();
                    row[3] = target.getCompany();
                    dtm.addRow(row);
                    targets.add(target);
                }
            }
        }
    }//GEN-LAST:event_searchbtnActionPerformed

    private void viewbtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_viewbtnActionPerformed
        // TODO add your handling code here:
        int selectedrow= tbl.getSelectedRow();
        if(selectedrow >=0){
            Flight f = (Flight)tbl.getValueAt(selectedrow,0);
            t1.setText(String.valueOf(f.getFlightid()));
            t2.setText(f.getDepart());
            t3.setText(f.getArrive());
            t4.setText(f.getFrom());
            t5.setText(f.getTo());
            t6.setText(f.getDate());
            t7.setText(f.getCompany());
            t8.setText(f.getModel());
            t9.setText(String.valueOf(f.getMadeyear()));
            t10.setText(String.valueOf(f.getSeatprice()));
        }else
            JOptionPane.showMessageDialog(null,"Please Select Any Flight");
    }//GEN-LAST:event_viewbtnActionPerformed


